int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt < 46341)
	{
		if (sqrt * sqrt == nb)
		{
			return (sqrt);
		}
		sqrt++;
	}
	return (0);
}
