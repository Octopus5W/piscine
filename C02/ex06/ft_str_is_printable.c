int	ft_str_is_printable(char *str)
{
	int	cpt;

	cpt = 0;
	while (str[cpt] != '\0')
	{
		if (str[cpt] < ' ' || str[cpt] > '~')
			return (0);
		cpt++;
	}
	return (1);
}
